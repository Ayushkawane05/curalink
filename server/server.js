const app = express();
app.use(cors());
app.use(express.json({ limit: "2mb" }));

// Mount upload route (Phase 4)
const uploadRouter = require("./routes/upload")(genAI);
app.use("/api/upload", uploadRouter);

// ─── Session Endpoints (Phase 3) ──────────────────────────────────────────────

/** GET /api/sessions — list sessions (metadata only, no messages) */
app.get("/api/sessions", async (_req, res) => {
  try {
    const sessions = await Session.find({})
      .select("sessionId title createdAt updatedAt")
      .sort({ updatedAt: -1 })
      .lean();
    res.json({ sessions });
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

/** POST /api/sessions — create a new empty session */
app.post("/api/sessions", async (_req, res) => {
  try {
    const session = await Session.create({});
    res.json({ sessionId: session.sessionId, title: session.title });
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

/** GET /api/sessions/:id — get full session with all messages */
app.get("/api/sessions/:id", async (req, res) => {
  try {
    const session = await Session.findOne({ sessionId: req.params.id }).lean();
    if (!session) return res.status(404).json({ error: "Session not found." });
    res.json(session);
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

/** DELETE /api/sessions/:id */
app.delete("/api/sessions/:id", async (req, res) => {
  try {
    await Session.deleteOne({ sessionId: req.params.id });
    res.json({ ok: true });
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

/** GET /api/documents — list uploaded documents */
app.get("/api/documents", async (_req, res) => {
  try {
    const docs = await Chunk.aggregate([
      { $match: { source: "upload", documentId: { $ne: null } } },
      { $group: {
        _id:    "$documentId",
        title:  { $first: "$title" },
        pmid:   { $first: "$pmid" },
        chunks: { $sum: 1 },
      }},
      { $sort: { _id: -1 } },
    ]);
    res.json({ documents: docs });
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
});

// ─── Main RAG Endpoint ────────────────────────────────────────────────────────

/**
 * POST /api/ask
 *
 * Body: { question: string, sessionId?: string }
 *
 * SSE stream events in order:
 *   1. "sources"    — retrieved source list (sent before generation for instant display)
 *   2. "chunk"      — token-by-token answer text
 *   3. "validation" — citation verification result (sent after full answer received)
 *   4. "chunk"      — medical disclaimer appended
 *   5. "done"       — stream complete
 */
app.post("/api/ask", async (req, res) => {
  const { question, sessionId } = req.body;

  if (!question || typeof question !== "string" || question.trim().length === 0) {
    return res.status(400).json({ error: "A non-empty 'question' field is required." });
  }

  // Set up SSE headers
  res.setHeader("Content-Type",  "text/event-stream");
  res.setHeader("Cache-Control", "no-cache");
  res.setHeader("Connection",    "keep-alive");
  res.flushHeaders();

  const send = (type, data = {}) => {
    res.write(`data: ${JSON.stringify({ type, ...data })}\n\n`);
  };

  try {
    // ── 1. Load session (Phase 3) ─────────────────────────────────────────
    let session = null;
    if (sessionId) {
      session = await Session.findOne({ sessionId });
      if (session) {
        // Compress old history if needed to keep context tight
        await maybeSummarizeSession(session);
      }
    }

    // ── 2. Retrieve top-K relevant chunks (ONE embed API call) ─────────────
    const topChunks = await retrieveChunks(question.trim());

    // ── 3. Build grounded prompt ───────────────────────────────────────────
    const { prompt, usedSources } = buildPrompt(question.trim(), topChunks, session);

    // ── 4. Send sources immediately (client renders them while streaming) ──
    send("sources", { sources: usedSources });

    // ── 5. Stream the Gemini response (accumulate for validation) ──────────
    let fullAnswer = "";
    const model  = genAI.getGenerativeModel({ model: "gemini-3.5-flash" });
    const result = await model.generateContentStream(prompt);

    for await (const chunk of result.stream) {
      const text = chunk.text();
      if (text) {
        send("chunk", { text });
        fullAnswer += text;
      }
    }

    // ── 6. Anti-hallucination validation (Phase 5) ─────────────────────────
    const validation = validateCitations(fullAnswer, usedSources);
    send("validation", validation);

    // ── 7. Append disclaimer and finish ───────────────────────────────────
    send("chunk", { text: MEDICAL_DISCLAIMER });
    send("done");

    // ── 8. Persist to session (Phase 3) ───────────────────────────────────
    if (session) {
      const fullContent = fullAnswer + MEDICAL_DISCLAIMER;

      session.messages.push({ role: "user", content: question.trim() });
      session.messages.push({
        role:       "assistant",
        content:    fullContent,
        sources:    usedSources,
        validation,
      });

      // Auto-title session from the first question asked
      if (session.messages.length === 2) {
        const q = question.trim();
        session.title = q.length > 60 ? q.slice(0, 60) + "…" : q;
      }

      session.markModified("messages");
      await session.save();
    }

  } catch (err) {
    console.error("❌  /api/ask error:", err);
    send("error", { message: err.message || "Internal server error." });
  } finally {
    res.end();
  }
});

